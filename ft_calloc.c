#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*thing;

	if (count <= 0)
		count = 1;
	if (size <= 0)
		size = 1;
	thing = malloc (count * size);
	if (thing)
		ft_bzero(thing, count * size);
	return (thing);
}
