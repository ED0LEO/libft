#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	c;

	c = 0;
	if (lst == 0)
		return (0);
	while (lst->next)
	{
		c++;
		lst = lst->next;
	}
	c++;
	return (c);
}
